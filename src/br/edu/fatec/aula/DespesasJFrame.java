    private void barchart() {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        Double rectable = 0.0;
        Double desptable = 0.0;

        for (int i = 0; i < modelo.getRowCount(); i++) {
            String valortable = String.valueOf(modelo.getValueAt(i, 1));
            String tipotable = String.valueOf(modelo.getValueAt(i, 3));

            if ("Receita".equals(tipotable)) {
                rectable += Double.parseDouble(valortable);
            } else {
                desptable += Double.parseDouble(valortable);
            }

        }

        dataset.addValue(rectable, "Receita", "");
        dataset.addValue(desptable, "Despesa", "");

        JFreeChart xy = ChartFactory.createBarChart("TIPO", "2018", "VALORES", dataset, PlotOrientation.VERTICAL,
                true,
                true,
                false);

        ChartPanel panel = new ChartPanel(xy);
        int width = tiporeceitaPanel.getWidth();
        int height = tiporeceitaPanel.getHeight();

        panel.setPreferredSize(new Dimension(width, height));

        tiporeceitaPanel.setLayout(new BorderLayout());
        tiporeceitaPanel.add(panel, BorderLayout.CENTER);
        tiporeceitaPanel.validate();
    }

    private void piechart() {
        DefaultPieDataset piedataset = new DefaultPieDataset();

        Double[] category = new Double[9];

        for (int i = 0; i < 9; i++) {

            category[i] = 0.0;
        }

        for (int i = 0; i < despesasTable.getRowCount(); i++) {
            String categoriatable = String.valueOf(modelo.getValueAt(i, 4));
            String value = String.valueOf(modelo.getValueAt(i, 1));
            Double num = Double.parseDouble(value);
            switch (categoriatable) {
                case "Compras":
                    category[0] += num;
                    break;
                case "Moradia":
                    category[1] += num;
                    break;
                case "Saúde":
                    category[2] += num;
                    break;
                case "Transporte":
                    category[3] += num;
                    break;
                case "Estudo":
                    category[4] += num;
                    break;
                case "Lazer":
                    category[5] += num;
                    break;
                case "Alimentação":
                    category[6] += num;
                    break;
                case "Outros":
                    category[7] += num;
                    break;
                case "Receita":
                    category[8] += num;
                    break;
                default:
                    break;
            }

        }

        if (category[0] > 0.0) {

            piedataset.setValue("Compras", category[0]);
        }

        if (category[1] > 0.0) {
            piedataset.setValue("Moradia", category[1]);
        }

        if (category[2] > 0.0) {
            piedataset.setValue("Saúde", category[2]);
        }
        if (category[3] > 0.0) {
            piedataset.setValue("Transporte", category[3]);
        }
        if (category[4] > 0.0) {
            piedataset.setValue("Estudo", category[4]);
        }
        if (category[5] > 0.0) {
            piedataset.setValue("Lazer", category[5]);
        }
        if (category[6] > 0.0) {
            piedataset.setValue("Alimentação", category[6]);
        }
        if (category[7] > 0.0) {
            piedataset.setValue("Outros", category[7]);
        }
        if (category[8] > 0.0) {
            piedataset.setValue("Receita", category[8]);
        }

        JFreeChart pie = ChartFactory.createPieChart("Categoria",
                piedataset,
                true,
                true,
                false);

        // PARTE PADRÃO DE TODOS OS GRÁFICOS 
        ChartPanel panel2 = new ChartPanel(pie);
        int width2 = categoriaPanel.getWidth();
        int height2 = categoriaPanel.getHeight();

        panel2.setPreferredSize(new Dimension(width2, height2));

        categoriaPanel.setLayout(new BorderLayout());
        categoriaPanel.add(panel2, BorderLayout.CENTER);
        categoriaPanel.validate();
    }

