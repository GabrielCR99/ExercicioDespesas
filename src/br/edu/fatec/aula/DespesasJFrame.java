    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:

        try {

            String valor = valorTextField.getText();

            double valorfinal = Double.parseDouble(valor.replace(",", ".").trim());

            if (receitaRadioButton.isSelected()) {

                receitatotal += valorfinal;

            } else {
                despesatotal += valorfinal;
            }

            receitatotalLabel.setText(String.valueOf(String.format("R$ %.2f", receitatotal)));
            despesatotalLabel.setText(String.valueOf(String.format("R$ %.2f", despesatotal)));
            saldototalLabel.setText(String.valueOf(String.format("R$ %.2f", receitatotal - despesatotal)));

            String tipo = null;

            String desc = descricaoTextField.getText();

            DateFormat df = new SimpleDateFormat("dd/MM/YYYY");
            String data = df.format(dataDateChooser.getDate());

            if (receitaRadioButton.isSelected()) {
                tipo = "Receita";

            } else {
                tipo = "Despesa";
            }

            String categoria = String.valueOf(categoriaComboBox.getSelectedItem());

            modelo = (DefaultTableModel) despesasTable.getModel();
            Object[] linha = {desc, valorfinal, data, tipo, categoria};
            modelo.addRow(linha);

            despesasTable.setModel(modelo);

            barchart();
            piechart();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Favor adicione valores",
                    "Valores",
                    JOptionPane.WARNING_MESSAGE);
        }

        novodado();

    }//GEN-LAST:event_addButtonActionPerformed

    private void excluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirButtonActionPerformed
        // TODO add your handling code here:

        int linhaSelecionada = despesasTable.getSelectedRow();
        if (linhaSelecionada < 0) {
            return;
        }

        DefaultTableModel modelo = (DefaultTableModel) despesasTable.getModel();

        String tipo = String.valueOf(modelo.getValueAt(linhaSelecionada, 3));
        String valor = String.valueOf(modelo.getValueAt(linhaSelecionada, 1));

        valor = valor.replace(',', '.').trim();
        Double valorfinal = Double.parseDouble(valor);

        if ("Receita".equals(tipo)) {
            receitatotal -= valorfinal;
        } else {
            despesatotal -= valorfinal;
        }

        receitatotalLabel.setText(String.valueOf(String.format("R$ %.2f", receitatotal)));
        despesatotalLabel.setText(String.valueOf(String.format("R$ %.2f", despesatotal)));
        saldototalLabel.setText(String.valueOf(String.format("R$ %.2f", receitatotal - despesatotal)));

        modelo.removeRow(linhaSelecionada);
        novodado();

    }//GEN-LAST:event_excluirButtonActionPerformed

