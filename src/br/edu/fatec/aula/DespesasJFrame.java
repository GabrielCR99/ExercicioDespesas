    private void refreshButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshButtonActionPerformed
        // TODO add your handling code here:

        int linhaSelecionada = despesasTable.getSelectedRow();
        modelo = (DefaultTableModel) despesasTable.getModel();

        if (linhaSelecionada >= 0) {

            modelo.setValueAt(descricaoTextField.getText(), linhaSelecionada, 0);
            modelo.setValueAt(valorTextField.getText(), linhaSelecionada, 1);
            modelo.setValueAt(dataDateChooser.getDate(), linhaSelecionada, 2);

            if (receitaRadioButton.isSelected()) {
                modelo.setValueAt("Receita", linhaSelecionada, 3);
            } else {
                modelo.setValueAt("Despesa", linhaSelecionada, 3);
            }

            modelo.setValueAt(categoriaComboBox.getSelectedItem(), linhaSelecionada, 4);

            piechart();
            barchart();

        } else {
            JOptionPane.showMessageDialog(null, "Error", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_refreshButtonActionPerformed

